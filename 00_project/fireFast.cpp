//============================================================
//	初期化処理
//============================================================
HRESULT CFireFast::Init(void)
{
	// 炎の初期化
	if (FAILED(CFire::Init()))
	{ // 初期化に失敗した場合

		// 失敗を返す
		assert(false);
		return E_FAIL;
	}

	// 成功を返す
	return S_OK;
}

//============================================================
//	終了処理
//============================================================
void CFireFast::Uninit(void)
{
	// 炎の終了
	CFire::Uninit();
}

//============================================================
//	挙動の更新処理
//============================================================
void CFireFast::UpdateMove(void)
{
	D3DXVECTOR3 pos = GetVec3Position();	// 位置

	// 位置の更新
	UpdatePosition(&pos);

	// 位置を反映
	SetVec3Position(pos);
}
